package day_3;

public class increment_Operator {

	public static void main(String[] args) {
		
		//post increment
		int a=10;
		
		int res= a++;  // in res variable it will store the value of a as a 10 and same it will print and later it will increment the value
		
		System.out.println(res);
		
	    int b=a;
		System.out.println(b);
		
		
		//pre increment
		
		int c= 100;
		
		int res1=++c;
		System.out.println(res1);
		
		
		//Post Decrement
		
		int x=200;
		
		int z= x--;
		
		System.out.println(z);
		
		

	}

}
